  	  <h1>Cream Puffs</h1>
  	  <h2>Recipe type: Dessert</h2>
  	  <div class="Crta"></div>
  	  <div class="Skrolaj">
  	    <div class="scrollbar"><div class="track"><div class="thumb"><div class="end"></div></div></div></div>
  	    <div class="viewport"><div class="overview">
  	      <p>Cream puffs can be considered one of Croatia’s staple desserts.  Here people call them “princess doughnuts”, which is a literal translation.  It might be because they resemble cute little princess cakes!<br>
There are a ton of recipes for this dessert. It’s almost a science. Why? Because of the pastry, or pâte à choux.  It can be bit tricky to make, but if you get it right then it is well worth the effort.  It should rise (be puffed) enough during baking so that its interior is not dense and doughy but instead hollow with a cavity that can be filled.  It shouldn’t burn but should still be baked enough so the inside is dry, and the end result should form a very light pastry that is oh-so-delicious.<br>
When it comes to filling, in Croatia we always make them with custard made of egg yolks, milk, cornstarch, sugar, vanilla and butter. Cornstarch is preferred over flour, as it gives the filling lightness and beautiful texture. Butter is preferred over margarine here because it makes the filling extra smooth, silky and rich. Oftentimes, we add a layer of whipped cream over the custard filling, although this can be omitted. A dusting of powdered sugar is always added on top. A lot of custard and whipped cream-it’s nice when these layers are visible, especially when you pipe them with pastry bag and star tip. Our cream puffs are always mile high!<br>
Tips: Always use vegetable or sunflower oil instead of butter or margarine instead of butter or margarine.  Also, play it safe and measure your eggs instead of counting them, so that you always have a ratio of 250ml eggs – 125 ml oil – 250 ml water – 150 g flour.
</p>
  	      <p>Ingredients:<br>
<strong>For the pastry</strong>
•	2 cups (250 ml) eggs (this is about 4 eggs size S)<br>
•	2 cups (250) ml water<br>
•	1/2 cup (125 ml) vegetable or sunflower oil<br>
•	1 1/8cup (150 g) all purpose flour<br>
•	pinch of salt<br>
•	3 tsp sugar<br>
<strong>For the filling</strong><br>
•	4 cups (1 l) milk<br>
•	3 sachets vanilla sugar (or 3 tsp vanilla extract)<br>
•	1 1/4 cup (250 g) sugar<br>
•	1 egg<br>
•	5 egg yolks<br>
•	1 1/8 cup (150 g) cornstarch<br>
•	4.2 oz (125 g) butter, softened, room temperature<br>
•	1 cup (250 ml) whipping cream + some powdered sugar<br>
•	powdered sugar for dusting</p>
  	      <p>Directions:<br>
1.	Make the pastry: Combine water, oil, salt and sugar and heat until it boils. Remove from heat and add flour, all at once, stirring with wooden spoon until all the lumps are dissolved and it pulls away from sides of the pan (it will take about 2 minutes).<br>
2.	Gradually add eggs, one by one and combine well after each addition, until the batter comes together. This is your pastry dough. It will be thick, shiny, smooth and sticky. Put it into a pastry bag with star tip or round plain tip and pipe it onto baking sheet lined with parchment paper, 1 inch apart. Make desired size mounts-I prefer their diameter to be 12.5 inch. Smooth pointy tops with a wet finger, so that they don’t burn while baking.<br>
3.	Preheat oven to 200 C and bake for 35-40 minutes. You can also bake them at 250 C for 12-14 minutes and then reduce temperature to 150 C and bake for 20 more minutes, so that the inside dries out thoroughly. Let cool completely and cut in half with serrated knife.<br>
4.	Whisk egg with yolks, 100 ml milk and cornstarch-make sure there are no lumps.<br>
5.	Heat the rest of milk with vanilla and sugar. When it boils, remove from heat and add to the egg mixture. Put on low heat and stir for several minutes until it thickens into a custard. Cover with plastic foil and let cool to room temperature.<br>
6.	Mix butter with electric mixer. Take cooled custard and mix it as well. Then combine the two. Make sure to thoroughly incorporate butter into the custard.<br>
7.	Mix whipping cream with some powdered sugar until soft peaks form.<br>
8.	Assembling:<br>
9.	Put the filling into a pastry bag with a star tip and pipe it on the bottom of each cream puff. Then pipe a layer of whipped cream and cover with tops you cut off earlier.<br>
10.	Dust with powdered sugar. Keep in cool place.</p>
