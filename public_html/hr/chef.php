  	      <p>Shrimp buzara is a Dalmatian die-hard’s dish.  This simple, succulent meal is a staple along the entire southern coastline and should not be missed.  Try serving it with a wonderful white wine like Korta Katarina’s Posip and you’re sure to impress guests with this local classic.</p>
  	      <p>Ingredients:<br>
•	1 kg shrimp<br>
•	10 g garlic (two garlic cloves, finely minced)<br>
•	4 tablespoons olive oil<br>
•	10 g raisins<br>
•	1/2 cup strained tomato<br>
•	1 cup fresh parsley leaves (minced)<br>
•	5 g salt<br>
•	pepper (to taste)<br>
•	1/2 liter white wine<br>
•	10 g breadcrumbs
  	        </p>
  	      <p>Directions:<br>
1.  Take a large pot, add olive oil and heat it, but not too much. Add garlic and fry just 30 seconds and then add all the shrimps. Using a wooden ladle mix the shrimps to coat them in oil.<br>
2.  Add wine and cook for 5 minutes until alcohol evaporates.  Add tomato, salt, raisins, pepper, and bread crumbs.  Mix gently with the ladle, or shake the whole pot to mix up all the ingredients.<br>
3.  Cover and cook for 10-20 minutes (depends on how large the shrimps are).  At the end add parsley leaves and serve.
