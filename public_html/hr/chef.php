  	  <h1>Crescents</h1>
  	  <h2>Recipe type: Dessert</h2>
  	  <div class="Crta"></div>
  	  <div class="Skrolaj">
  	    <div class="scrollbar"><div class="track"><div class="thumb"><div class="end"></div></div></div></div>
  	    <div class="viewport"><div class="overview">
  	      <p>Prep time: 1 hour; Cook time: 20 mins<br>
Total time: 1 hour 20 mins<br>
For some reason we call these desserts crescents in Croatia (Kresenti). They are made of flavorful, melt-in-your-mouth dough, linked by dark or white chocolate, or also with marmalade. This recipe is made with dark and white chocolate, in which 2 drops of almond extract are added.<br>
The dough is formed into tiny balls, dipped into egg whites, rolled into ground almonds/ walnuts or hazelnuts, baked and then linked with chocolate or jelly. Heaven! This dough is wildly flavorful due to rum, orange/lemon zest, and vanilla bourbon. The cookies are best after a day or two, as they tend to soften a bit and all the flavors come together.</p>
  	      <p>Ingredients:<br>
<strong>For the dough</strong><br>
•	12.3 oz (350 g) all purpose flour<br>
•	7 oz (200 g) butter<br>
•	5.2 oz (150 g) sugar<br>
•	1 vanilla bean pod<br>
•	zest of 1 lemon<br>
•	zest of 1/2 orange<br>
•	1 egg<br>
•	2 egg yolks<br>
•	2 tbsp rum<br>
<strong>For the filling</strong><br>
•	3.5 oz (100 g) dark chocolate<br>
•	1.4 oz (40 g) butter<br>
•	you can also use 3.5 oz (100 g) white chocolate<br>
•	or your favorite marmalade<br>
<strong>For the coating</strong><br>
•	2 egg whites<br>
•	5.2 oz (150 g) ground almonds</p>
  	      <p>Directions:<br>
1.	For the dough, mix butter with sugar, seeds from vanilla bean, eggs and yolks, lemon and orange zest, and rum. Mix well, then add sifted flour and make the dough. When the dough comes together (it will be creamy and soft) wrap it in plastic wrap and leave in the fridge for at least 1 hour.<br>
2.	Preheat oven to 350°F, or 180°C<br>
3.	Take out the well-chilled dough and form little balls. Dip them in egg whites (you can just slightly beat them with your fork) and then roll in the almonds. Place them on a baking sheet lined with parchment paper. Bake for 20 minutes.  Let cool while you make the filling.<br>
4.	Melt the dark chocolate with butter and let cool for 10 minutes. Use this to join 2 balls. You can also link them with white chocolate that you melt in your microwave or with your favorite marmalade.</p>
