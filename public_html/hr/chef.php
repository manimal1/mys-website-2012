2.	Slice them into short, thin strips.<br>
3.	Finely chop one onion and cook it with two whole cloves of garlic in a saucepan with the Olive Oil.<br>
4.	When the garlic is brown, remove it from the pan and add finely chopped parsley and the squid.<br>
5.	Fry the squid slowly, then drizzle with a little white wine and its black ink. Halfway through cooking add the rice, stir and pour in the tomato sauce.<br>
6.	Slowly mix in the hot broth until the rice has cooked completely.<br>
7.	Taste before removing from heat. Add salt and pepper as needed. Serve immediately.
