•	Curry powder to taste<br>
•	A little sugar</p>
		<p>Preparation of the marinade<br>
1.  Mix the ingredients with lemon juice, and then pour over the olive oil little by little. Blend until desired consistency is achieved. The sauce should taste simply of olive oil and lemon juice.<br>
2.  The pieces of swordfish for this dish should be cut from the dorsal fin. Remove the bones and the skin and leave the meat in the freezer for 40 minutes. Place a small layer of sauce in a porcelain dish and put the fillets cut into thin slices in a row. Leave in the fridge for 48 hours before serving.</p>
		<p>Presentation of the dish<br>
Scatter a handful of rocket at the bottom of a dish, place some tomato slices over the top and then add the thin slices of swordfish. Drizzle the marinade over the dish and garnish with basil, lemon and black olives.</p>
